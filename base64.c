int base64decode (char *in, size_t inLen, unsigned char *out, size_t *outLen) {

    char *end = in + inLen;
    char iter = 0;
    size_t buf = 0, len = 0;

    while (in < end) {
        unsigned char c = d[(int)(*in)];
        in++;

        switch (c) {
            case WHITESPACE: continue;
            case INVALID:    return 1;
            case EQUALS:
                in = end;
                continue;
            default:
                buf = buf << 6 | c;
                iter++;

                if (iter == 4) {
                    if ((len += 3) > *outLen) return 1;
                    *(out++) = (buf >> 16) & 255;
                    *(out++) = (buf >> 8) & 255;
                    *(out++) = buf & 255;
                    buf = 0; iter = 0;

                }
        }
    }

    if (iter == 3) {
        if ((len += 2) > *outLen) return 1;
        *(out++) = (buf >> 10) & 255;
        *(out++) = (buf >> 2) & 255;
    }
    else if (iter == 2) {
        if (++len > *outLen) return 1;
        *(out++) = (buf >> 4) & 255;
    }

    *outLen = len;
    return 0;
}